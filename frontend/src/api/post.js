import request from './request'

export const postApi = {
  boards: () => request.get('/boards'),
  list: params => request.get('/posts', { params }),
  hot: params => request.get('/posts/hot', { params }),
  search: params => request.get('/posts/search', { params }),
  detail: id => request.get(`/posts/${id}`),
  exists: id => request.get(`/posts/${id}/exists`),
  create: data => request.post('/posts', data),
  checkSimilar: data => request.post('/posts/check-similar', data),
  update: (id, data) => request.put(`/posts/${id}`, data),
  remove: id => request.delete(`/posts/${id}`),
  createDraft: data => request.post('/posts/drafts', data),
  updateDraft: (id, data) => request.put(`/posts/drafts/${id}`, data),
  draftDetail: id => request.get(`/posts/drafts/${id}`),
  myDrafts: params => request.get('/posts/drafts/mine', { params }),
  publishDraft: (id, data) => request.post(`/posts/drafts/${id}/publish`, data),
  deleteDraft: id => request.delete(`/posts/drafts/${id}`),
  like: id => request.post(`/posts/${id}/like`),
  unlike: id => request.delete(`/posts/${id}/like`),
  favorite: id => request.post(`/posts/${id}/favorite`),
  unfavorite: id => request.delete(`/posts/${id}/favorite`),
  report: (id, data) => request.post(`/posts/${id}/report`, data),
  favorites: params => request.get('/posts/favorites', { params }),
  followingFeed: params => request.get('/posts/following-feed', { params }),
  myLikes: params => request.get('/posts/likes/mine', { params }),
  myReports: params => request.get('/reports/mine', { params }),
  myPosts: params => request.get('/posts/mine', { params }),
  replies: (postId, sort) => request.get(`/posts/${postId}/replies`, { params: sort ? { sort } : {} }),
  createReply: (postId, data) => request.post(`/posts/${postId}/replies`, data),
  likeReply: id => request.post(`/replies/${id}/like`),
  unlikeReply: id => request.delete(`/replies/${id}/like`),
  dislikeReply: id => request.post(`/replies/${id}/dislike`),
  undislikeReply: id => request.delete(`/replies/${id}/dislike`),
  deleteReply: id => request.delete(`/replies/${id}`),
  reportReply: (id, data) => request.post(`/replies/${id}/report`, data),
  myReplies: params => request.get('/replies/mine', { params }),
  replyPostId: id => request.get(`/replies/${id}/post-id`),
  replyPreview: id => request.get(`/replies/${id}/preview`),
  pinProfile: id => request.post(`/posts/${id}/profile-pin`),
  unpinProfile: id => request.delete(`/posts/${id}/profile-pin`),
  pinOfficial: id => request.post(`/posts/${id}/official-pin`),
  unpinOfficial: id => request.delete(`/posts/${id}/official-pin`),
  pinReply: (postId, replyId) => request.post(`/posts/${postId}/pin-reply/${replyId}`),
  unpinReply: postId => request.delete(`/posts/${postId}/pin-reply`)
}
